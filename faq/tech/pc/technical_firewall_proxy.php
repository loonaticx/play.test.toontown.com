    <td valign="top"><p>
      <font size="2" face="Arial, Helvetica, sans-serif"><strong><em><font color="#0000FF">Q:</font> Can
      I play Toontown through a network proxy or firewall?</em></strong></font>
      <p><em><strong><font size="2" face="Arial, Helvetica, sans-serif"><font color="#0000FF">A:</font></font></strong><font size="2" face="Arial, Helvetica, sans-serif"></font></em><font size="2" face="Arial, Helvetica, sans-serif"> Yes,
          but your firewall or proxy must allow traffic on ports 80 (the standard
          http port), 443 (the standard secure https port), and port 6667, which
          Toontown uses to connect to its game server. If you have products such
          as Norton Internet Security, ZoneAlarm, BlackICE, McAfee Firewall,
          or Microsoft WinXP Internet Connection Firewall enabled, Toontown may
          not be able to connect to its servers and the game may &quot;hang&quot; while
          it is trying to download content or start up, so make sure any such
          products are configured correctly.
<br>
<p>
<strong>McAfee Security Center and Firewalls</strong>
<ol> 
<li>Double Click on the Red Box with an M in the taskbar.
<li>Select the Personal Firewall + Tab.
<li>Click on the "View the Internet Applications List" icon. A new window will open up.
<li>Click on the Utilities Tab.
<li>Click on the Security Settings Icon.
<li>Change your current settings to "Trusting."
</ol>
	  If you've set the firewall to "Trusting" but still aren't able to log into Toontown, the permission level may need to be changed. To change a permission level:
<ol>
	  <li>Right-click the "McAfee" icon, point to Personal Firewall, then click Internet Applications.
<li>In the Permissions list, right-click the permission level for an application, and choose a different level.
<li>Click "Allow Full Access" to allow the application to both send and receive data.
</ol>
If you need additional help configuring your McAfee product to work with Toontown, please contact <a href="http://ts.mcafeehelp.com/default.asp?siteID=1">McAfee technical support</a>.
</p>
<br/>
<p>
<strong>Norton Internet Security Configuration</strong>:
<ol>
<li> Double click on the "world" icon in your taskbar. 
<li>Select Personal Firewall.
<li>Click Configure.
<li>Select the Programs tab at the top.
<li>Select the Add button.
<li>Navigate to C:-> Program files -> Disney -> Disney Online -> Toontown.
<li>Select Toontown.exe and click Open.
<li>Select Permit from the drop down box and click ok.
<li>On the next screen, select Networked Games and click OK.
<li>Click OK again to exit.
</ol>
If problems continue after following these steps, we recommend checking the <a href="http://www.symantec.com/home_homeoffice/support/selectproduct_ts.jsp">Symantec Home & Home Office Support pages</a>.
</p>
<br/>
<p>
<strong>ZoneAlarm</strong>

<ol>
<li>When you try to log into Toontown with ZoneAlarm installed, you'll see an alert box that says "NEW PROGRAM Toontown is trying to access the Internet."
<li>When the box appears, put a check mark in the box next to "Remember this setting," then click "Allow."
</ol>
If you receive error messages that say a firewall has blocked Toontown, you may need to change Toontown's permission setting in ZoneAlarm:
<ol>
<li>Double-click the ZA icon in the bottom right corner of the screen. 
<li>When the ZoneAlarm window appears, click the Program Control tab on the left side of the window.
<li>Click the Programs tab in the upper right corner of the window.
<li>Make sure there are four green checks on the Toontown line. If you see red Xs or question marks, left click them and choose "Allow."
</ol>
If problems continue after following these steps, we recommend checking the <a href="http://www.zonelabs.com/store/content/support/support.jsp">ZoneLabs support pages</a>.
</p>

