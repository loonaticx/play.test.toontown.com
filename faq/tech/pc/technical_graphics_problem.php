      <font size="2" face="Arial, Helvetica, sans-serif"><strong><em><font color="#0000FF">Q:</font> What
      if I experience
graphics problems with Toontown? </em></strong></font>
      <p><em><strong><font size="2" face="Arial, Helvetica, sans-serif"><font color="#0000FF">A:</font> </font></strong><font size="2" face="Arial, Helvetica, sans-serif"></font></em><font size="2" face="Arial, Helvetica, sans-serif">First
          check that your graphics card is not on the list of unsupported cards.
          Otherwise, there may be a problem with your video card driver. Make
          sure you install the latest one available on your manufacturer's Web
          site. </font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">To determine which video card you have installed on your computer:</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">
	- Click the &quot;Start&quot; button in the lower left hand corner.<br>
	- Select &quot;Run&quot;<br>
	- In the resulting box type: dxdiag <br>
	- Click the &quot;OK&quot; button. <br>
	- Select the &quot;Display&quot; tab at the top. <br>
	- Under Device you will find the name and manufacturer of your video card.<br>
	- Under Drivers you will find the date and version of the video card driver installed in your computer. <br>
	- Once you know the name of the video card manufacturer, you can search for the
  		latest driver by visiting your favorite search engine and entering the &quot;(manufacturer's
  		name) video drivers&quot; in the search field. <br>
	- Visit the manufacturer's website for instructions on installing the latest video drivers.</font></p>

	<p><font size="2" face="Arial, Helvetica, sans-serif">Also install the
          latest DirectX version from:<br>
            <a href="http://www.microsoft.com/directx">http://www.microsoft.com/directx</a>.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">Windows 2000/XP users,
          please install the latest MS Service Pack for your OS (<a href="http://windowsupdate.microsoft.com/">available
          here</a>).</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">If your Toontown
          mouse cursor is missing/drawn improperly, you can try switching to
          the regular Windows cursor. If you are running Win9x/ME click on Start-&gt;Run-&gt; Type:
          command.exe. If you are running Windows 2000/XP click Start-&gt;Run-&gt;Type:
          cmd.exe. In the resulting window type: cd &quot;C:\Program Files\Disney\Disney
          Online\Toontown&quot; Configrc.exe -cursor_off -save . Then try starting
          Toontown as normal. </font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">Another thing to
          try for graphics issues is switching Toontown's default graphics API.
          Sometimes video drivers support one API better than another. This can
          be done by clicking Start-&gt;Run-&gt; type: Configrc.exe [XXX] -save
          where [XXX] is -OGL, -DX8, or -DX7.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">If you try these
          fixes and are still having trouble, please submit a problem report,
          and attach the log files then do the following: </font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">Press the F9 key
          in the game to automatically save a screenshot image in a file named &quot;[frame_number].jpg&quot; which
          will be saved in &quot;C:\Program Files\Disney\Disney Online\Toontown.&quot; <br>
  Exit Toontown, go to the Start Menu, select Run, and type dxdiag.exe. <br>
  Click the Save All Information button, which saves the file DxDiag.txt, and
  then exit. <br>
  Attach the files &quot;&quot;DxDiag.txt&quot;&quot; and &quot;&quot;[frame_number].jpg&quot;&quot; to
  the bug report, along with a description of the problem, and e-mail to toontown@disneyonline.com <br>
  You can also use the Report Problem page to do this.</font></p>
