    <td valign="top"><p>
      <font size="2" face="Arial, Helvetica, sans-serif"><strong><em><font color="#0000FF">Q:</font> Do
      I need any special equipment, such as a 3-D card, to play Toontown?</em></strong></font>
      <p><em><strong><font size="2" face="Arial, Helvetica, sans-serif"><font color="#0000FF">A:</font> </font></strong><font size="2" face="Arial, Helvetica, sans-serif"></font></em><font size="2" face="Arial, Helvetica, sans-serif">Yes,
          you need a 3-D video card, but don't worry about checking your computer.
          The Toontown installer will confirm that all necessary hardware and
          software is in place prior to installation.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">The following 3-D
          video cards have been tested by our QA staff and appear to work with
          Disney's Toontown Online. Other cards may work, of course. This is
          not an endorsement for any of these products.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif"><br>
  ATI Radeon 7000 (or greater) <br>
  ATI Rage 128 Pro <br>
  ATI Rage 128 <br>
  NVidia Riva TNT/TNT2 <br>
  NVidia Geforce 2/3/4 <br>
  Matrox Millenium G200 <br>
  Matrox Millenium G400 <br>
  S3/Diamond Viper II <br>
  S3/Diamond Savage 4 <br>
  3Dfx Voodoo 3/4/5 </font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif"><br>
  To determine which video card you have installed on your computer:</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">
- Click the &quot;Start&quot; button in the lower left hand corner.<br>
- Select &quot;Run.&quot;<br>
- In the resulting box type: dxdiag <br>
- Click the &quot;OK&quot; button. <br>
- Select the &quot;Display&quot; tab at the top. <br>
- Under Device, you will find the name and manufacturer of your video card.<br>
- Under Drivers, you will find the date and version of the video card driver installed in your computer. <br>
- Once you know the name of the video card manufacturer, you can search for the
  latest driver by visiting your favorite search engine and entering the &quot;(manufacturer's
  name) video drivers&quot; in the search field. <br>
- Visit the manufacturer's Web site for instructions on installing the latest video drivers.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">The following older
          3-D video cards are not recommended for use with Toontown and are not
          officially supported. Toontown may have problems displaying text, random
          graphics errors, or serious slowdown performance issues with these
          cards. If you have problems with one of these cards with the latest
          drivers, your best choice is to install a newer video card.</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">ATI Rage Pro <br>
  ATI Rage Pro Mobility <br>
  ATI Rage XL <br>
  Other ATI Rage Pro chipset-based cards - (Some Rage Pros lack some transparency
  support, and may not display readable text) <br>
  Intel I-810 <br>
  Intel I-815 <br>
  Any PCI video card with less than 16MB video memory </font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif"><br>
  The following 3-D video cards will not work properly with Toontown:</font></p>
      <p><font size="2" face="Arial, Helvetica, sans-serif">Any card with less
          than 4MB of video memory <br>
  Number Nine Imagine Series 2 <br>
  3Dfx Voodoo 1, 2 (no mouse cursor) <br>
  S3 Virge-based cards </font></p>
